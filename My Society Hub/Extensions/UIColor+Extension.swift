import UIKit

extension UIColor {
    
    class func pagerBlueColor() -> UIColor{
        return UIColor(named: "pagerBlue")!
    }
    
    class func pagerGreenColor() -> UIColor{
        return UIColor(named: "pagerGreen")!
    }
    
    class func pagerLightBlueColor() -> UIColor{
        return UIColor(named: "pagerLightBlue")!
    }
    
    class func pagerOrangeColor() -> UIColor{
        return UIColor(named: "pagerOrange")!
    }
    
    class func appOrangeColor() -> UIColor{
        return UIColor(named: "appOrangeColor")!
    }
    
    class func blueBorderColor() -> UIColor{
        return UIColor(named: "blueBorderColor")!
    }
    
}
